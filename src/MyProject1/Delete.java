    private void formnoTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formnoTextFieldKeyReleased
        // TODO add your handling code here:
        try{
            rs=null;         
            pstmt=MyLogin.con.prepareStatement("select FORM_NO,FIRST_NAME,LAST_NAME from ADMISSION_TABLE where FORM_NO=? and S_ID='-'",ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE); 
            pstmt.setInt(1, Integer.parseInt(formnoTextField.getText().trim()));
            rs=pstmt.executeQuery();
            pstmt.clearParameters();
            if(rs.next()){      
                    msgLabel.setText("");
                    BufferedImage originalImg=ImageIO.read(new File(".\\test\\Record\\Student_Images\\Admission\\"+rs.getString("FIRST_NAME")+" "+rs.getString("LAST_NAME")+""+rs.getString("FORM_NO")+".jpg"));    // to read image do this               
                    ImageIcon icon=new ImageIcon(ImageProcessing.myResize(originalImg,imageLabel.getWidth(),imageLabel.getHeight()));
                    imageLabel.setIcon(icon);    
            }
            else{
                msgLabel.setText("*Invalid form number");
                imageLabel.setIcon(null);
            }
        }catch(Exception e){System.out.println(""+e);}
    }//GEN-LAST:event_formnoTextFieldKeyReleased

