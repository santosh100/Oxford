    public void myDetails(){
        try{
            pstmt=MyLogin.con.prepareStatement("select * from ADMISSION_TABLE where A_YEAR=? and S_ID='-' order by FORM_NO",ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);            
            pstmt.setString(1, ""+AdminControl.year);          
            rs=pstmt.executeQuery();         
            pstmt.clearParameters();
            
            DefaultTableModel tm=(DefaultTableModel)deleteTable.getModel();
            while(true)
            {
                int c=tm.getRowCount();
                if(c==0)
                    break;
                else
                     tm.removeRow(0);
            }            
            while(rs.next()){                
                Object kk[]={rs.getString("FORM_NO"),rs.getString("FIRST_NAME")+" "+rs.getString("LAST_NAME"),rs.getString("FATHER_NAME"),rs.getString("ADDRESS")};
                tm.addRow(kk);
            }
            yearjLabel.setText(""+AdminControl.year);            
        }
        catch(Exception e){
            System.out.println("problem in printing table OR ZERO RECORD"+e);            
        }
    }           
